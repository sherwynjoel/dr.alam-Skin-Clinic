<?php
/**
 * Dr. Alam's Skin Clinic - Appointment Handler
 * Destination: admin@dralamdermcentre.com
 */

// Allow from any origin for development, but ideally restrict to your domain
header("Access-Control-Allow-Origin: *");
header("Content-Type: application/json; charset=UTF-8");

if ($_SERVER["REQUEST_METHOD"] == "POST") {
    // 1. Destination Email
    $to = "admin@dralamdermcentre.com";
    $subject = "Clinic Appointment Request - " . strip_tags($_POST["Full_Name"]);

    // 2. Form Data
    $name = strip_tags(trim($_POST["Full_Name"]));
    $phone = strip_tags(trim($_POST["Phone_Number"]));
    $service = strip_tags(trim($_POST["Service"]));
    $doctor = strip_tags(trim($_POST["Doctor"]));
    $date = strip_tags(trim($_POST["Preferred_Date"]));
    $time = strip_tags(trim($_POST["Preferred_Time"]));

    // 3. Email Content (Formatted for readability)
    $email_content = "NEW APPOINTMENT REQUEST\n";
    $email_content .= "====================================\n\n";
    $email_content .= "PATIENT INFORMATION:\n";
    $email_content .= "Name: $name\n";
    $email_content .= "Phone: $phone\n\n";
    
    $email_content .= "APPOINTMENT DETAILS:\n";
    $email_content .= "Service: $service\n";
    $email_content .= "Doctor: $doctor\n";
    $email_content .= "Date: $date\n";
    $email_content .= "Time: $time\n\n";
    
    $email_content .= "====================================\n";
    $email_content .= "Generated by Clinic Website at " . date("H:i, d-m-Y") . "\n";

    // 4. Headers (Crititcal for delivery)
    $headers = "From: Web Concierge <noreply@dralamdermcentre.com>\r\n";
    $headers .= "Reply-To: $name <$phone>\r\n";
    $headers .= "X-Mailer: PHP/" . phpversion();

    // 5. Send Email
    if (mail($to, $subject, $email_content, $headers)) {
        http_response_code(200);
        echo json_encode(["status" => "success", "message" => "Sent to admin@dralamdermcentre.com"]);
    } else {
        http_response_code(500);
        echo json_encode(["status" => "error", "message" => "Mail server failed to deliver."]);
    }

} else {
    http_response_code(403);
    echo json_encode(["status" => "error", "message" => "Invalid Request"]);
}
?>
